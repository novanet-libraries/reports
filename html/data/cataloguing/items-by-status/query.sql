SELECT
  TITLE, AUTHOR, IMPRINT, YEAR, ISN, BIB_NUMBER,
  CALLNUMBER, DESCRIPTION, COLLECTION,
  BARCODE, ITEM_STATUS, PROCESS_STATUS
FROM WEBREPORT.ITEM_RECORDS
LEFT OUTER JOIN WEBREPORT.ADM_BIB_LOOKUP ON ITEM_RECORDS.ADM_NUMBER = ADM_BIB_LOOKUP.ADM_NUMBER
LEFT OUTER JOIN WEBREPORT.SHORT_RECORDS ON SHORT_RECORDS.BIB_NUMBER = ADM_BIB_LOOKUP.BIB_NUMBER
WHERE SUB_LIBRARY = :SUBLIBRARY
  AND COLLECTION = :COLLECTION
  AND ITEM_STATUS IN ( :STATUS )
  AND ROWNUM <= 50000
