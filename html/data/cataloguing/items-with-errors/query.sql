SELECT
  TITLE, AUTHOR, IMPRINT, YEAR, ISN, BIB_NUMBER,
  CALLNUMBER, DESCRIPTION, COLLECTION,
  BARCODE, MATERIAL_TYPE, ITEM_STATUS, PROCESS_STATUS,
  Z30_UPDATE_DATE LAST_EDIT
FROM WEBREPORT.ITEM_RECORDS
NATURAL JOIN WEBREPORT.ADM_BIB_LOOKUP
NATURAL JOIN WEBREPORT.SHORT_RECORDS
WHERE (SUB_LIBRARY IS NULL OR SUB_LIBRARY NOT IN ( :SUBLIBRARIES ))
OR    (COLLECTION  IS NULL OR COLLECTION  NOT IN ( :COLLECTIONS ))
OR  (MATERIAL_TYPE IS NULL OR MATERIAL_TYPE NOT IN ( :MATERIALS ))
OR  (ITEM_STATUS IS NOT NULL AND RTRIM(ITEM_STATUS) != '' AND ITEM_STATUS NOT IN ( :ISTATUSES ))
OR  (PROCESS_STATUS IS NOT NULL AND RTRIM(PROCESS_STATUS) != '' AND PROCESS_STATUS NOT IN ( :PSTATUSES ))
