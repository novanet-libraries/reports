SELECT
  TITLE, AUTHOR, IMPRINT, YEAR, ISN, BIB_NUMBER,
  CALLNUMBER, DESCRIPTION, COLLECTION,
  BARCODE, MATERIAL_TYPE, ITEM_STATUS, PROCESS_STATUS, LAST_EDIT
FROM WEBREPORT.ITEM_RECORDS
NATURAL JOIN WEBREPORT.ADM_BIB_LOOKUP
NATURAL JOIN WEBREPORT.SHORT_RECORDS
WHERE SUB_LIBRARY    NOT IN ( :SUBLIBRARIES )
OR    COLLECTION     NOT IN ( :COLLECTIONS )
OR    MATERIAL_TYPE  NOT IN ( :MATERIALS )
OR    ITEM_STATUS    NOT IN ( :ISTATUSES )
OR    PROCESS_STATUS NOT IN ( :PSTATUSES )
